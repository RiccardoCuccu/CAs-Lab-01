#include "LPC17xx.h"
#include "led.h"

/*----------------------------------------------------------------------------
  Function that switch on LEDs 4 and 11
 *----------------------------------------------------------------------------*/

void led4and11_On(void)
{
	LPC_GPIO2->FIOSET |= 0x00000081;
}

void led4_Off(void)
{
	LPC_GPIO2->FIOCLR |= 0x00000001;
}
